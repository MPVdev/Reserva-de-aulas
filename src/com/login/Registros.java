    private void ButtonBusquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonBusquedaActionPerformed

        try {
            Connection conexion = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/reservadeaulas", "root", "");
            Statement statement = (Statement) conexion.createStatement();
            
            String consulta = "select res_id,res_fecha,res_horainicio,res_horafin,usu_id,aula_id,edi_id from reserva where ";
            if (!txtFecha.getText().isEmpty()) {
                consulta += "res_fecha LIKE '%" + txtFecha.getText() + "%' AND ";
            }
            if (!txtHoraInicio.getText().isEmpty()) {
                consulta += "res_horainicio LIKE '%" + txtHoraInicio.getText() + "%' AND ";
            }
            if (!txtHoraFin.getText().isEmpty()) {
                consulta += "res_horafin LIKE '%" + txtHoraInicio.getText() + "%' AND ";
            }
            if (!txtUsuario.getText().isEmpty()) {
                consulta += "usu_id LIKE '%" + txtUsuario.getText() + "%' AND ";
            }
            if (!txtAula.getText().isEmpty()) {
                consulta += "aula_id LIKE " + txtAula.getText() + " AND ";
            }
            
            // Eliminar el último 'AND' de la cadena de consulta
            consulta = consulta.substring(0, consulta.length() - 5);
            ResultSet rs = statement.executeQuery(consulta);
            DefaultTableModel model = (DefaultTableModel) TablaRegistros.getModel();
            model.setRowCount(0); // Limpiamos los datos existentes en la tabla
            while (rs.next()) {
                String[] datos = new String[7];
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                model.addRow(datos);
            }
            rs.close();
            statement.close();
            conexion.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_ButtonBusquedaActionPerformed

