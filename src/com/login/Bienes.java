    private void ButtonModificarBienesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonModificarBienesActionPerformed
        try {
            // Conectar a la base de datos
            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/reservadeaulas", "root", "");

            // Realiza el comando Mysql
            String sqlUpdate = "update bienes set bie_nombre=?, bie_descripcion=?, aula_id=? where bie_id=?";
            PreparedStatement stmt = (PreparedStatement) conn.prepareStatement(sqlUpdate);
            stmt.setString(1, txtNombreBienes.getText());
            stmt.setString(2, txtDescripcionBienes.getText());
            stmt.setString(3, txtAula.getText());
            stmt.setString(4, txtIDBienes.getText());
            int rowsAffected = stmt.executeUpdate();

            if (rowsAffected > 0) {
                // Mensaje de confirmación
                JOptionPane.showMessageDialog(null, "Registro actualizado exitosamente");
            } else {
                // Mensaje de error
                JOptionPane.showMessageDialog(null, "La ID no existe");
            }
            // Cerrar la conexión
            stmt.close();
            conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        mostrar();
    }//GEN-LAST:event_ButtonModificarBienesActionPerformed

