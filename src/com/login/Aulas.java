    private void ButtonModificarAulasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonModificarAulasActionPerformed
        try {
            // Conectar a la base de datos
            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/reservadeaulas", "root", "");

            // Realiza el comando Mysql
            String sqlUpdate = "update aula set aula_capacidad=?, edi_id=? where aula_id=?";
            PreparedStatement stmt = (PreparedStatement) conn.prepareStatement(sqlUpdate);
            stmt.setString(1, txtCapacidadAulas.getText());
            stmt.setString(2, txtEdificioAulas.getText());
            stmt.setString(3, txtIDAula.getText());
            int rowsAffected = stmt.executeUpdate();

            if (rowsAffected > 0) {
                // Mensaje de confirmación
                JOptionPane.showMessageDialog(null, "Registro actualizado exitosamente");
            } else {
                // Mensaje de error
                JOptionPane.showMessageDialog(null, "La ID no existe");
            }
            // Cerrar la conexión
            stmt.close();
            conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        mostrar();
    }//GEN-LAST:event_ButtonModificarAulasActionPerformed

