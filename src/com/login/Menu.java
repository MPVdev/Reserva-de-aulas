    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed

        try {
             //conectar la base de datos
             Connection conn= (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/reservadeaulas", "root", "");
             
             String sqlReserva = "insert into reserva (res_fecha, res_horainicio, res_horafin,usu_id,aula_id,edi_id) values (?,?,?,?,?,?)";
             PreparedStatement stmt= (PreparedStatement) conn.prepareStatement(sqlReserva);
             stmt.setString(1, ((JTextField)res_fecha.getDateEditor().getUiComponent()).getText());
             stmt.setString(2, ((JTextField)cbxHorainicio.getEditor().getEditorComponent()).getText());
             stmt.setString(3, ((JTextField)cbnHorafinal.getEditor().getEditorComponent()).getText());
             stmt.setInt(4, usuId);
             stmt.setString(5, ((JTextField)cbxAula.getEditor().getEditorComponent()).getText());
             stmt.setString(6, ((JTextField)cbxEdificio.getEditor().getEditorComponent()).getText());
             
             int rowsAffected = stmt.executeUpdate();
             if (rowsAffected > 0) {
                // Mensaje de confirmaci√≥n
                JOptionPane.showMessageDialog(null, "Reserva Ingresado exitosamente");
            } else {
                // Mensaje de error
                JOptionPane.showMessageDialog(null, "");
            }
             
         }catch (SQLException e) {
            e.printStackTrace();
        }
         
    }//GEN-LAST:event_btnReservarActionPerformed

